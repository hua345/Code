package com.github.chenjianhua.publishhelloworld;

public class ResultInfo {
	private String name;
	private int age;
	
	public ResultInfo(){
		this.name = "fangfang";
		this.age = 22;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}

}
